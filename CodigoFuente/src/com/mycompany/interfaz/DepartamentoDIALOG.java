private void mostrarDepartamentos() {
        this.limpiarTabla();
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.addColumn("Identificador");
        modelo.addColumn("Nombre");

        try {
            ResultSet resultado = this.crud.seleccionar(SQL.seletTodosDepartamento());

            while (resultado.next()) {

                Object[] depAux = {resultado.getInt(1), resultado.getString(2)};//Obtenemos el id y el nombre  
                modelo.addRow(depAux);
            }//cierre de while

            this.jTable1.setModel(modelo);

        } catch (SQLException e) {
            System.out.println("Error:" + e.getMessage());

        }

    }//cierre del metodo mostrarDepartamentos

    
    private void limpiarTabla() {
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{}
        ));

    }

