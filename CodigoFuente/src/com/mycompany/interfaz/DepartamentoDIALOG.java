    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        boolean resultado = this.crud.eliminarInsertar(SQL.eliminarDepartamento(this.idSeleccionado));

        if (resultado) {
            JOptionPane.showMessageDialog(null, "Eliminado correctamente");
            this.mostrarDepartamentos();
        } else {
            JOptionPane.showMessageDialog(null, "No eliminado correctamente");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:

        this.departamento = new Departamento();
        this.crud = new CRUD();

        String dep = this.txtDepartamento.getText();
        this.departamento.setNombre(dep);

        //Guardar en la base de datos

        boolean resultado = this.crud.insertar(SQL.insertDepartamento(this.departamento));
        this.txtDepartamento.setText("");

        if(resultado){
            JOptionPane.showMessageDialog(null,"Insertado correcatamente");
            this.mostrarDepartamentos();
        }else{
            JOptionPane.showMessageDialog(null,"No insertado");
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
       

        this.departamento = new Departamento();

        this.departamento.setNombre(this.txtDepartamento.getText());
        this.departamento.setId_departamento(this.idSeleccionado);

        boolean resultado = this.crud.eliminarInsertar(SQL.actualizarDepartamento(departamento));

        if (resultado) {
            JOptionPane.showMessageDialog(null, "Actualizado correctamente");
            this.mostrarDepartamentos();
        } else {
            JOptionPane.showMessageDialog(null, "No actualizado correctamente");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        this.btnActualizar.setEnabled(true);
        this.btnEliminar.setEnabled(true);

        int fila = this.jTable1.getSelectedRow();

        this.idSeleccionado = Integer.parseInt(this.jTable1.getValueAt(fila, 0).toString());

        System.out.println(this.idSeleccionado);

        try {
            ResultSet resultado = this.crud.seleccionar(SQL.selectDepartamento(this.idSeleccionado));

            if (resultado.next()) {

                this.txtDepartamento.setText(resultado.getString(2));
                btnInsertar.setEnabled(false);

            }

        } catch (SQLException e) {
            System.out.println("Error" + e.getMessage());
        }
    }//GEN-LAST:event_jTable1MouseClicked

